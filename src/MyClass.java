import java.util.*;
public class MyClass {
	public static void main(String[] args) {
		// Initialise a scanner
		Scanner keyboard = new Scanner (System.in);
		
		// Record some input
		String blah;
		blah = keyboard.nextLine();
		System.out.println("You said " + blah);
		
		// Record some more input
		String numero;
		numero = keyboard.nextLine();
		System.out.println("Then you said " + numero);
		
	}
}

